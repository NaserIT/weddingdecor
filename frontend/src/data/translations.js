export const getTranslation = (lang, path) => {
  const keys = path.split('.');
  let result = translations[lang];
  for (const key of keys) {
    if (result && result[key]) {
      result = result[key];
    } else {
      return path; // Return path if translation not found
    }
  }
  return result;
};
